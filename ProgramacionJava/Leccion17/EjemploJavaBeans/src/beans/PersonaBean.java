package beans;

import java.io.Serializable;

/**
 *
 * @author almis
 */
public class PersonaBean implements Serializable {
    
    private String nombre;
    private int edad;

    public PersonaBean() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
    
}
